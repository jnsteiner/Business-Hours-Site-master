Wouldn’t it be great if every store you ever went to was always open 24 hours?  This would save all of us from having to rush to the doors of our local market before they lock up, or it could save us from driving to a store that’s already closed for the evening. Obviously, this utopia does not exist. We are left with some stores opened with limited hours during certain holidays, some closed on Mondays, while some with early closings on weekends, or extended hours during the Thanksgiving-Christmas rush.  It’s memory overload! </p>

